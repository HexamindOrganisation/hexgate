"""Tests for ``PolicyBundle`` — loading and integrity verification.

The bundle abstraction is what the runtime hands to the WASM evaluator.
These tests need a real ``opa`` binary because they build bundles end-to-
end (yaml → rego → wasm) before exercising the loader. The
``needs_opa`` mark skips them in environments without opa.
"""

from __future__ import annotations

import hashlib
import json
import shutil
from pathlib import Path

import pytest

from fortify.security import (
    BundleIntegrityError,
    BundleLoadError,
    BundleSignatureError,
    PolicyBundle,
    WasmPolicy,
    compile_to_rego,
    compile_to_wasm,
    generate_keypair,
    sign_bytes,
)


_OPA_AVAILABLE = shutil.which("opa") is not None
needs_opa = pytest.mark.skipif(
    not _OPA_AVAILABLE,
    reason="opa not on PATH — install via `brew install opa` to run these tests",
)


# ---------------------------------------------------------------------------
# Fixtures
# ---------------------------------------------------------------------------


_DEMO_YAML = """\
version: 1
roles:
  billing:
    tools:
      refund_order:
        mode: allow
        constraints:
          - args.amount <= 500
"""


def _build_bundle(directory: Path, stem: str = "policy") -> Path:
    """Build a real bundle on disk under ``directory`` and return its path."""
    yaml_path = directory / f"{stem}.yaml"
    yaml_path.write_text(_DEMO_YAML, encoding="utf-8")
    source_hash = hashlib.sha256(_DEMO_YAML.encode("utf-8")).hexdigest()

    rego_text = compile_to_rego(
        {
            "version": 1,
            "roles": {
                "billing": {
                    "tools": {
                        "refund_order": {
                            "mode": "allow",
                            "constraints": ["args.amount <= 500"],
                        }
                    }
                }
            },
        },
        source_hash=source_hash,
    )
    wasm = compile_to_wasm(rego_text).wasm

    (directory / f"{stem}.rego").write_text(rego_text, encoding="utf-8")
    (directory / f"{stem}.wasm").write_bytes(wasm)
    manifest = {
        "version": 1,
        "source": f"{stem}.yaml",
        "source_hash": source_hash,
        "rego_hash": hashlib.sha256(rego_text.encode("utf-8")).hexdigest(),
        "wasm_hash": hashlib.sha256(wasm).hexdigest(),
    }
    (directory / f"{stem}.bundle.json").write_text(
        json.dumps(manifest, indent=2, sort_keys=True) + "\n", encoding="utf-8"
    )
    return directory


@pytest.fixture
def bundle_dir(tmp_path: Path) -> Path:
    if not _OPA_AVAILABLE:
        pytest.skip("opa not on PATH")
    return _build_bundle(tmp_path)


def _sign_bundle(directory: Path, private_key_raw: bytes, stem: str = "policy") -> None:
    """Write a detached signature over the manifest's exact on-disk bytes."""
    manifest_bytes = (directory / f"{stem}.bundle.json").read_bytes()
    sig = sign_bytes(manifest_bytes, private_key_raw)
    (directory / f"{stem}.bundle.json.sig").write_bytes(sig)


