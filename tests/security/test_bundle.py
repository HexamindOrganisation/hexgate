@needs_opa
def test_verify_integrity_passes_for_fresh_bundle(bundle_dir: Path) -> None:
    """A bundle built by the CLI verifies cleanly with no tampering."""
    bundle = PolicyBundle.from_disk(bundle_dir)
    bundle.verify_integrity()  # raises on failure


@needs_opa
def test_verify_integrity_catches_tampered_wasm(bundle_dir: Path) -> None:
    """Flip one byte in the wasm file — verify catches the mismatch."""
    bundle = PolicyBundle.from_disk(bundle_dir)
    # Tamper after loading by editing the in-memory bytes
    bundle.wasm_bytes = bundle.wasm_bytes[:-1] + bytes([(bundle.wasm_bytes[-1] ^ 1)])
    with pytest.raises(BundleIntegrityError, match="wasm hash mismatch"):
        bundle.verify_integrity()


@needs_opa
def test_verify_integrity_catches_tampered_rego(bundle_dir: Path) -> None:
    """Edit the rego text after load — verify catches the mismatch."""
    bundle = PolicyBundle.from_disk(bundle_dir)
    bundle.rego_text = bundle.rego_text + "\n# tampered\n"
    with pytest.raises(BundleIntegrityError, match="rego hash mismatch"):
        bundle.verify_integrity()


@needs_opa
def test_verify_integrity_catches_tampered_source(bundle_dir: Path) -> None:
    """Edit the on-disk yaml after build — verify catches it."""
    bundle = PolicyBundle.from_disk(bundle_dir)
    bundle.source_path.write_text("version: 1\nroles: {}\n", encoding="utf-8")
    with pytest.raises(BundleIntegrityError, match="source.yaml hash mismatch"):
        bundle.verify_integrity()


@needs_opa
def test_verify_integrity_refuses_no_wasm_manifest(bundle_dir: Path) -> None:
    """Manifests built with --no-wasm have wasm_hash=null — refuse them
    for enforcement use."""
    bundle = PolicyBundle.from_disk(bundle_dir)
    bundle.manifest["wasm_hash"] = None
    with pytest.raises(BundleIntegrityError, match="--no-wasm"):
        bundle.verify_integrity()


