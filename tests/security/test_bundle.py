@needs_opa
def test_from_disk_loads_all_artifacts(bundle_dir: Path) -> None:
    """All four files populate the dataclass fields correctly."""
    bundle = PolicyBundle.from_disk(bundle_dir)
    assert bundle.source_path.name == "policy.yaml"
    assert bundle.rego_text.startswith("# Generated by fortify policy build")
    assert bundle.wasm_bytes.startswith(b"\x00asm")
    assert bundle.manifest["version"] == 1


def test_from_disk_rejects_non_directory(tmp_path: Path) -> None:
    """Loader refuses a path that isn't a directory at all."""
    target = tmp_path / "nope"
    with pytest.raises(BundleLoadError, match="not a directory"):
        PolicyBundle.from_disk(target)


def test_from_disk_rejects_empty_directory(tmp_path: Path) -> None:
    """Empty dir → missing manifest error, not a silent fall-through."""
    with pytest.raises(BundleLoadError, match="no .*bundle.json"):
        PolicyBundle.from_disk(tmp_path)


@needs_opa
def test_from_disk_rejects_ambiguous_directory(
    bundle_dir: Path, tmp_path: Path
) -> None:
    """Two bundles in one dir → refuse rather than guess."""
    _build_bundle(bundle_dir, stem="other")
    with pytest.raises(BundleLoadError, match="multiple bundle manifests"):
        PolicyBundle.from_disk(bundle_dir)


@needs_opa
def test_from_disk_rejects_partial_bundle(bundle_dir: Path) -> None:
    """Manifest present but rego missing → caller gets a specific error."""
    (bundle_dir / "policy.rego").unlink()
    with pytest.raises(BundleLoadError, match="missing policy.rego"):
        PolicyBundle.from_disk(bundle_dir)


